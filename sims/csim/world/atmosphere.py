def density(alt: float, solar_activity: SolarActivity = SolarActivity.MEAN):
    """Atmospheric density using MSISE-90 Model of Earth's Upper Atmosphere

    http://www.braeunig.us/space/atmos.htm

    Args:
        alt (float): Altitude [m]
        solar_activity (SolarActivity, optional): Solar activity. Defaults to SolarActivity.MEAN.

    Returns:
        float: Density [kg/m3]
    """
    alt /= 1000 # To get to km
    match solar_activity:
        case SolarActivity.LOW:
            return interp(alt, low_solar_df["alt_km"], low_solar_df["density_kg_m3"])
        case SolarActivity.MEAN:
            return interp(alt, mean_solar_df["alt_km"], mean_solar_df["density_kg_m3"])
        case SolarActivity.HIGH:
            return interp(alt, high_solar_df["alt_km"], high_solar_df["density_kg_m3"])

